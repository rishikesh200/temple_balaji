// Helper to convert names to URL slugs
export const slugify = (text) => {
  if (!text) return "";
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)+/g, "");
};

// Helper to retrieve pooja by slug or ID
export const getPoojaBySlug = (slug) => {
  if (!slug) return null;
  const all = [...dailyPoojas, ...specialSevas, ...nerthikadans];
  const targetSlug = slug.toLowerCase();
  return all.find(
    (p) => p.id.toLowerCase() === targetSlug || slugify(p.name) === targetSlug
  );
};
